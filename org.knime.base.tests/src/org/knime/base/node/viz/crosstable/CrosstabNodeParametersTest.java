package org.knime.base.node.viz.crosstable;

import java.io.FileInputStream;
import java.io.IOException;

import org.knime.core.data.DataColumnSpecCreator;
import org.knime.core.data.DataTableSpec;
import org.knime.core.data.def.DoubleCell;
import org.knime.core.data.def.StringCell;
import org.knime.core.node.InvalidSettingsException;
import org.knime.core.node.NodeSettings;
import org.knime.core.node.port.PortObjectSpec;
import org.knime.core.webui.node.dialog.SettingsType;
import org.knime.core.webui.node.dialog.defaultdialog.NodeParametersUtil;
import org.knime.testing.node.dialog.DefaultNodeSettingsSnapshotTest;
import org.knime.testing.node.dialog.SnapshotTestConfiguration;

@SuppressWarnings("restriction")
final class CrosstabNodeParametersTest extends DefaultNodeSettingsSnapshotTest {

    CrosstabNodeParametersTest() {
        super(getConfig());
    }

    private static SnapshotTestConfiguration getConfig() {
        return SnapshotTestConfiguration.builder() //
            .withInputPortObjectSpecs(createInputPortSpecs()) //
            .testJsonFormsForModel(CrosstabNodeParameters.class) //
            .testJsonFormsWithInstance(SettingsType.MODEL, () -> readSettings()) //
            .testNodeSettingsStructure(() -> readSettings()) //
            .build();
    }

    private static CrosstabNodeParameters readSettings() {
        try {
            var path = getSnapshotPath(CrosstabNodeParameters.class).getParent().resolve("node_settings")
                .resolve("CrosstabNodeParameters.xml");
            try (var fis = new FileInputStream(path.toFile())) {
                var nodeSettings = NodeSettings.loadFromXML(fis);
                return NodeParametersUtil.loadSettings(
                    nodeSettings.getNodeSettings(SettingsType.MODEL.getConfigKey()),
                    CrosstabNodeParameters.class);
            }
        } catch (IOException | InvalidSettingsException e) {
            throw new IllegalStateException(e);
        }
    }

    private static PortObjectSpec[] createInputPortSpecs() {
        return new PortObjectSpec[]{createDefaultTestTableSpec()};
    }

    private static DataTableSpec createDefaultTestTableSpec() {
        return new DataTableSpec(
            new DataColumnSpecCreator("Universe_0_0", StringCell.TYPE).createSpec(),
            new DataColumnSpecCreator("Universe_0_1", StringCell.TYPE).createSpec(),
            new DataColumnSpecCreator("Universe_1_0", DoubleCell.TYPE).createSpec());
    }
}
